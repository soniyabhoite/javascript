var numbers=[10,20,30,40,50]
console.log("array elements")
console.log(numbers)
console.log(numbers[0])

//push and pop
//push-> add ele in last position of array
//pop -> remove the ele from last position of array
console.log("push")
numbers.push(60)
numbers.push(70)
console.log(numbers)
console.log("pop")
numbers.pop()
console.log(numbers)

//shift and unshift 
//shift -> remove the ele from first postion of array
//unshift -> add ele at first position of array

numbers.shift()
console.log("shift")
console.log(numbers)
console.log("unshift")
numbers.unshift(100,200)
console.log(numbers)

